#include "../Includes/libft.h"

void	ft_putnbr(int n)
{
	unsigned int i;

	i = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
	}
	i = n;
	if (i > 9)
		ft_putnbr(i / 10);
	ft_putchar(i % 10 + '0');
}
